# T45: interface-shape assertions for extracted-contracts-ok (Python lane).
#
# Exercises the ABC/Protocol contract idiom emitted by the Python backend for the
# my.ok.extracted.contracts fixture (extracted-contracts-ok/).
#
# Three assertion classes per requirement:
#   (a) Contract variant — a concrete type that implements B is accepted through a
#       B-typed annotation and its B-declared member is readable via that reference.
#   (b) Mirror variant — B exists as a standalone ABC, the concrete type does NOT
#       inherit it, but B's declared-member interface is verified by introspection.
#   (c) Member-set spot-check — abstract method names match the documented fields.
#
# All failure paths raise explicitly; no `assert` statements (vacuous under -O).
# Generated by `mdl :test-gen-regular-adt`; hand-written under RuntimeTests/ so
# pytest collects it via the `BaboonTests/RuntimeTests` discover pass in
# test-python-regular.

from __future__ import annotations

import inspect
import unittest


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _require(condition: bool, message: str) -> None:
    """Unconditional raise on failure — never vacuous unlike `assert`."""
    if not condition:
        raise AssertionError(message)


def _abstract_method_names(cls) -> set:
    """Return the set of @abstractmethod names declared directly on cls."""
    return {
        name
        for name, _ in inspect.getmembers(cls, predicate=inspect.isfunction)
        if getattr(getattr(cls, name), '__isabstractmethod__', False)
    }


